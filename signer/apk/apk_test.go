func TestValidateZIPOption(t *testing.T) {
	var testcases = []struct {
		input  string
		result error
	}{
		{ZIPMethodCompressAll, nil},
		{ZIPMethodCompressPassthrough, nil},
		{"", errors.New("Invalid APK ZIP option")},
	}
	for i, testcase := range testcases {
		testcase := testcase // capture range variable
		t.Run(fmt.Sprintf("GetOptions (%d) input: %#v", i, testcase.input), func(t *testing.T) {
			t.Parallel()

			err := validateZIPOption(testcase.input)
			if testcase.result == nil && err != nil {
				t.Fatalf("ValidateZIPOption returned unexpected error %s", err)
			} else if testcase.result != nil && err.Error() != testcase.result.Error() {
				t.Fatalf("Expected GetOptions to err with msg %s but got %s", testcase.result.Error(), err.Error())
			}
		})
	}
}

func TestSignFile(t *testing.T) {
	t.Parallel()

	// initialize a signer
	s, err := New(apksignerconf)
	if err != nil {
		t.Fatalf("signer initialization failed with: %v", err)
	}
	if s.Config().Type != apksignerconf.Type {
		t.Fatalf("signer type %q does not match configuration %q", s.Config().Type, apksignerconf.Type)
	}
	if s.Config().ID != apksignerconf.ID {
		t.Fatalf("signer id %q does not match configuration %q", s.Config().ID, apksignerconf.ID)
	}
	if s.Config().PrivateKey != apksignerconf.PrivateKey {
		t.Fatalf("signer private key %q does not match configuration %q", s.Config().PrivateKey, apksignerconf.PrivateKey)
	}

	// sign input file
	signedAPK, err := s.SignFile(testAPK, s.GetDefaultOptions())
	if err != nil {
		t.Fatalf("failed to sign file: %v", err)
	}
	zipReader := bytes.NewReader(signedAPK)
	r, err := zip.NewReader(zipReader, int64(len(signedAPK)))
	if err != nil {
		t.Fatal(err)
	}
	var (
		sigstr  string
		sigdata []byte
	)
	for _, f := range r.File {
		switch f.Name {
		case "META-INF/SIGNATURE.SF":
			rc, err := f.Open()
			defer rc.Close()
			if err != nil {
				t.Fatal(err)
			}
			sigdata, err = ioutil.ReadAll(rc)
			if err != nil {
				t.Fatal(err)
			}
		case "META-INF/SIGNATURE.RSA":
			rc, err := f.Open()
			defer rc.Close()
			if err != nil {
				t.Fatal(err)
			}
			rawsig, err := ioutil.ReadAll(rc)
			if err != nil {
				t.Fatal(err)
			}
			sigstr = base64.StdEncoding.EncodeToString(rawsig)
		}
	}
	// convert string format back to signature
	sig2, err := Unmarshal(sigstr, sigdata)
	if err != nil {
		t.Fatalf("failed to unmarshal signature: %v", err)
	}

	// make sure we still have the same string representation
	sigstr2, err := sig2.Marshal()
	if err != nil {
		t.Fatalf("failed to re-marshal signature: %v", err)
	}
	if sigstr != sigstr2 {
		t.Fatalf("marshalling signature changed its format.\nexpected\t%q\nreceived\t%q",
			sigstr, sigstr2)
	}
	// verify signature on input data
	if sig2.Verify() != nil {
		t.Fatalf("failed to verify apk signature: %v", sig2.Verify())
	}
}

